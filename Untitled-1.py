class Животное:
    def __init__(self, имя, вид, команды, дата_рождения):
        self.__имя = имя
        self.__вид = вид
        self.__команды = команды
        self.__дата_рождения = дата_рождения

    def get_имя(self):
        return self.__имя

    def set_имя(self, имя):
        self.__имя = имя

    def get_вид(self):
        return self.__вид

    def set_вид(self, вид):
        self.__вид = вид

    def get_команды(self):
        return self.__команды

    def set_команды(self, команды):
        self.__команды = команды

    def add_команды(self, новые_команды):
        self.__команды.extend(новые_команды)

    def get_дата_рождения(self):
        return self.__дата_рождения

    def set_дата_рождения(self, дата_рождения):
        self.__дата_рождения = дата_рождения


class Домашнее_животное(Животное):
    pass


class Вьючное_животное(Животное):
    pass


class Реестр:
    def __init__(self):
        self.животные = []

    def завести_животное(self, животное):
        self.животные.append(животное)

    def список_команд(self, имя):
        for животное in self.животные:
            if животное.get_имя() == имя:
                return животное.get_команды()
        return None

    def обучить_командам(self, имя, новые_команды):
        for животное in self.животные:
            if животное.get_имя() == имя:
                животное.add_команды(новые_команды)


def main():
    реестр = Реестр()
    while True:
        print("\n1. Завести новое животное")
        print("2. Увидеть информацию о животном")
        print("3. Увидеть список команд")
        print("4. Обучить животное новым командам")
        print("5. Выйти")
        выбор = input("Выберите действие: ")

        if выбор == "1":
            имя = input("Введите имя: ")
            вид = input("Введите вид: ")
            команды = input("Введите команды (разделенные запятой): ").split(", ")
            дата_рождения = input("Введите дату рождения: ")
            if вид.lower() in ["собака", "кошка", "хомяк"]:
                животное = Домашнее_животное(имя, вид, команды, дата_рождения)
            else:
                животное = Вьючное_животное(имя, вид, команды, дата_рождения)
            реестр.завести_животное(животное)

        elif выбор == "2":
            имя = input("Введите имя: ")
            найдено = False
            for животное in реестр.животные:
                if животное.get_имя() == имя:
                    print(f"Имя: {животное.get_имя()}, Вид: {животное.get_вид()}, Дата рождения: {животное.get_дата_рождения()}")
                    найдено = True
                    break
            if not найдено:
                print("Животное не найдено.")

        elif выбор == "3":
            имя = input("Введите имя: ")
            команды = реестр.список_команд(имя)
            if команды:
                print(f"Список команд для {имя}: {', '.join(команды)}")
            else:
                print("Животное не найдено или у него нет команд.")

        elif выбор == "4":
            имя = input("Введите имя: ")
            новые_команды = input("Введите новые команды (разделенные запятой): ").split(", ")
            реестр.обучить_командам(имя, новые_команды)
            print(f"Животное {имя} обучено новым командам.")

        elif выбор == "5":
            break

        else:
            print("Неверный выбор. Пожалуйста, выберите еще раз.")


if __name__ == "__main__":
    main()